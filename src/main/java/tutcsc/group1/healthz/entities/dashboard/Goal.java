/**
 * Represents the fitness goals a user can have.
 */
public enum Goal {

    /** General health maintenance goal. */
    GENERAL_HEALTH,

    /** Goal to lose weight. */
    WEIGHT_LOSS,

    /** Goal to gain weight. */
    WEIGHT_GAIN,

    /** Goal to gain muscle mass. */
    MUSCLE_GAIN
}
