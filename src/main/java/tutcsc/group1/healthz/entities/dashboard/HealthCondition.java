package tutcsc.group1.healthz.entities.dashboard;

/**
 * Represents common health conditions that a user may have.
 */
public enum HealthCondition {

    /** User has diabetes. */
    DIABETES,

    /** User has hypertension (high blood pressure). */
    HYPERTENSION,

    /** User has a heart disease condition. */
    HEART_DISEASE,

    /** User has a thyroid condition. */
    THYROID,

    /** User has no specific health condition. */
    NONE
}
